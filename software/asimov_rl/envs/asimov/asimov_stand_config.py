    """Configuration for Asimov v1 humanoid standing/walking task (legs only)."""

    class env(LeggedRobotCfg.env):
        # Observation stack: 66 historical frames feed the long-history CNN,
        # 5 short frames feed the state estimator, 3 critic frames feed the
        # privileged critic. Matches X1 layout because we run 12 active DOF.
        frame_stack = 66
        short_frame_stack = 5
        c_frame_stack = 3
        num_single_obs = 47          # 5 cmd + 12 q + 12 dq + 12 a + 3 omega + 3 euler
        num_observations = int(frame_stack * num_single_obs)
        single_num_privileged_obs = 73
        single_linvel_index = 53
        num_privileged_obs = int(c_frame_stack * single_num_privileged_obs)
        num_actions = 12
        num_envs = 4096
        episode_length_s = 24
        use_ref_actions = False
        num_commands = 5             # sin_phase, cos_phase, vx, vy, wz

    class safety:
        pos_limit = 1.0
        vel_limit = 1.0
        torque_limit = 0.85

