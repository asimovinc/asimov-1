    class init_state(LeggedRobotCfg.init_state):
        # Asimov standing height: pelvis_link at 0.63 m (from MJCF default pose).
        # X1 used 0.7 m — Asimov is slightly shorter at the hip.
        pos = [0.0, 0.0, 0.66]

        # Default standing pose: balanced squat with COM over feet.
        # Asimov's pelvis_link origin sits ~5 cm forward of the actual hip
        # joints (an artifact of the MJCF convention — see body_mass diagnostic
        # in tools/test_default_pose.py). Earlier attempts to copy X1's
        # hip_pitch=+0.4 produced a 27 cm forward COM offset because Asimov's
        # +Y hip rotation swings the leg backward, not forward.
        #
        # Configuration here uses the standard "balanced squat" formula:
        #   hip_pitch + ankle_pitch ≈ knee/2 (in magnitude), opposite signs
        # so the shank stays roughly vertical and the foot stays under the hip.
        # Sign of hip_pitch is negative to push the foot forward (because +Y
        # rotation here moves the leg backward), and knee positive to bend
        # toward flexion. Verified with tools/test_default_pose.py.
        default_joint_angles = {
            # COM-centering fix: previous pose (hip_pitch ±0.20) put COM at 28%
            # from heel — leaving only 6.2cm back-margin vs 15.6cm front-margin
            # (foot length 21.8cm including toe). Any forward swing during
            # walking pushed COM past the heel → backward tipping (仰天摔倒).
            # Reduced hip_pitch magnitude from 0.20 to 0.10 to lean torso
            # slightly forward, recentering COM at 50% of foot support polygon.
            # Verified: front-margin 10.9cm, back-margin 10.7cm (balanced).
            'left_hip_pitch_joint':   -0.20,    # was -0.10 — sweep_default_pose: ±0.20 puts COM 1.6cm fwd of ankle mid (±0.10 was 6.1cm)
            'left_hip_roll_joint':    0.10,
            'left_hip_yaw_joint':     0.0,
            'left_knee_joint':        0.40,
            'left_ankle_pitch_joint':-0.20,     # match hip to keep shank vertical
            'left_ankle_roll_joint':  0.0,
            'right_hip_pitch_joint':  0.20,     # mirror
            'right_hip_roll_joint':  -0.10,
            'right_hip_yaw_joint':    0.0,
            'right_knee_joint':      -0.40,
            'right_ankle_pitch_joint': 0.20,    # mirror
            'right_ankle_roll_joint': 0.0,
        }

    class control(LeggedRobotCfg.control):
        control_type = 'P'

        # PD gains.
        # Note: hip_pitch was kp=30 (X1 baseline) but test_lift_foot.py showed
        # hip_pitch could not pull the leg into a -0.6 target — only reached
        # -0.31 (50% tracking error). The required torque to lift a 6.4 kg leg
        # with COM at 0.25m is ~16 N·m; kp=30 with realistic error of 0.2 only
        # provides 6 N·m. Doubled to kp=60 so hip_pitch can actually pull the
        # leg forward against gravity.
        stiffness = {
            'hip_pitch_joint':   100,    # X1: 30 -> 100 (3.3x): test_lift_foot showed kp=30 only achieved 50% target tracking
            'hip_roll_joint':    40,     # X1: 40 (kept) — too high kp makes lateral lean unstable
            'hip_yaw_joint':     35,
            'knee_joint':        200,    # X1: 100 -> 200 (2x): need to support full body weight during single-foot phase
            'ankle_pitch_joint': 50,     # X1: 35 -> 50: ankle strategy stability
            'ankle_roll_joint':  40,     # X1: 35 -> 40: foot-roll stability during single-foot
        }
        damping = {
            'hip_pitch_joint':   8,      # scale with kp
            'hip_roll_joint':    3.0,    # X1 baseline
            'hip_yaw_joint':     4,
            'knee_joint':        15,
            'ankle_pitch_joint': 1.0,
            'ankle_roll_joint':  3.0,
        }

        action_scale = 0.5
        decimation = 10   # sim @ 1000 Hz, policy @ 100 Hz

